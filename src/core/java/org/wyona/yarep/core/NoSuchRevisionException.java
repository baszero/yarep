package org.wyona.yarep.core;

/**
 *
 */
public class NoSuchRevisionException extends RepositoryException {

    /**
     *
     */
    public NoSuchRevisionException() {
        super();
    }

    /**
     *
     */
    public NoSuchRevisionException(Throwable t) {
        super(t);
    }

    /**
     *
     */
    public NoSuchRevisionException(String s) {
        super(s);
    }

    /**
     *
     */
    public NoSuchRevisionException(String s, Throwable t) {
        super(s, t);
    }

}
