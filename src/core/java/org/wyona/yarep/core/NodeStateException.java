package org.wyona.yarep.core;

/**
 *
 */
public class NodeStateException extends RepositoryException {

    /**
     *
     */
    public NodeStateException() {
        super();
    }

    /**
     *
     */
    public NodeStateException(Throwable t) {
        super(t);
    }

    /**
     *
     */
    public NodeStateException(String s) {
        super(s);
    }

    /**
     *
     */
    public NodeStateException(String s, Throwable t) {
        super(s, t);
    }

}
